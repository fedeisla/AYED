package tp2.Ejercicio9;

public class Dato {
	
	private int suma;
	private int diferencia;
	
	
	public Dato(int suma, int diferencia) {
		this.suma = suma;
		this.diferencia = diferencia;
	}
	public int getSuma() {
		return suma;
	}
	public void setSuma(int suma) {
		this.suma = suma;
	}
	public int getDiferencia() {
		return diferencia;
	}
	public void setDiferencia(int diferencia) {
		this.diferencia = diferencia;
	}
	
	
}
